using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Web;

namespace OpenXC.Web.Models
{
    /// <summary>
    /// View model for logged data.
    /// </summary>
    public class LoggedDataViewModel
    {
        /// <summary>
        /// The date/time the data was logged (UTC).
        /// </summary>
        [Display(Name = "Date Logged")]
        public DateTime LoggedTime { get; set; }

        /// <summary>
        /// The data that was logged.
        /// </summary>
        [Display(Name = "Logged Data")]
        public string Data { get; set; }
    }
}